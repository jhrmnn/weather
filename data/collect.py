#!/usr/bin/env python3
"""Collect raw Open-Meteo ensemble responses into a per-run archive.

Each stored file is the raw API response plus a single added ``model_run_time``
stamp (see :func:`fetch_raw`), keyed by the run's initialisation time *and* a
hash of its forecast content so each distinct forecast is archived exactly once.
The collector reads the model metadata first and downloads the ensemble only
when the latest run is not already stored, so a new file (and a new commit)
appears only when a genuinely new run is available — which is what lets CI push
to the ``data`` branch only when there is something new.

Layout::

    <data-dir>/<model>/<lat>_<lon>/<run-init-time>-<content-hash>.json

``<run-init-time>`` is the run's UTC initialisation time (``YYYYMMDDTHHMMSSZ``),
so files sort chronologically and the most recent one is "the latest run".
``<content-hash>`` fingerprints the forecast data itself (see
:func:`content_hash`), so a run whose data merely duplicates an earlier one —
the model-run stamp can advance without the data changing — is never archived or
plotted twice. Older ``<run-init-time>.json`` files without a hash are still
read, and gain a content-keyed copy on the next collection.
"""
from __future__ import annotations

import argparse
import datetime as dt
import hashlib
import json
import os
import time
from dataclasses import dataclass

import requests

ENSEMBLE_URL = "https://ensemble-api.open-meteo.com/v1/ensemble"
# Per-model static metadata (run timestamps etc.); the forecast payload itself
# carries no run time, so the real initialisation time is read from here.
META_URL = "https://api.open-meteo.com/data/{model}/static/meta.json"
MODEL = "ecmwf_ifs025"

# Time format used for the model-run stamp attached to each payload; matches
# Open-Meteo's own ``hourly.time`` strings (UTC, e.g. ``2026-06-22T12:00``).
RUN_TIME_FORMAT = "%Y-%m-%dT%H:%M"

# How many times to re-fetch the data when a new run lands mid-fetch (see
# ``fetch_raw``); a tiny window, so one retry almost always suffices.
RUN_FETCH_RETRIES = 3

# Per-request resilience: the Open-Meteo endpoints occasionally time out or
# return a transient 5xx, and a run archives several locations in one go (each a
# few HTTP calls), so a single flaky response shouldn't fail the whole job.
# Retry the GET a few times with exponential backoff before giving up.
REQUEST_RETRIES = 4
REQUEST_BACKOFF = 2.0  # seconds; doubled after each failed attempt
REQUEST_TIMEOUT = 60  # seconds per attempt

# Filename key: ``<run-stamp>-<content-hash>``. The run stamp (the run's UTC
# initialisation time, YYYYMMDDTHHMMSSZ) keeps files human-readable and
# chronologically sortable; the content hash makes the archive's *identity* the
# forecast data itself, so a run whose data duplicates an earlier one is never
# stored — or plotted — twice (see ``content_hash``).
STAMP_FORMAT = "%Y%m%dT%H%M%SZ"
CONTENT_HASH_LEN = 12

# Fields excluded from the content hash: the API's per-response timing and the
# run stamp we attach. Everything else (valid times + every member series) is
# the forecast, and is what defines a run's identity.
_VOLATILE_KEYS = ("generationtime_ms", "model_run_time")

README = """\
# Raw forecast data archive

This orphan branch is an archive of **raw Open-Meteo Ensemble API responses**,
collected automatically by the `fetch` job of the site workflow.

Each file is the API payload, plus an added `model_run_time` stamp (the run's
UTC initialisation time). Files are keyed by that run init time *and* a hash of
their forecast content, so each distinct forecast is stored once; a new file
(and a new commit) appears only when a genuinely new run becomes available.

```
<model>/<lat>_<lon>/<run-init-time>-<content-hash>.json
```

The content hash means a run whose data merely duplicates an earlier one (the
model-run stamp can advance without the data changing) is never archived twice.
Older `<run-init-time>.json` files without a hash are still read, and gain a
content-keyed copy on the next collection.

Do not edit by hand — it is regenerated by CI.
"""


@dataclass(frozen=True)
class Location:
    latitude: float
    longitude: float
    name: str | None = None


# Points to archive. Each becomes a selectable city on the published site (the
# site renders one figure pair per location); add more entries here to grow the
# archive and the city selector.
LOCATIONS = [
    Location(52.55, 13.41, "Berlin"),
    Location(50.08, 14.44, "Prague"),
    Location(48.81, 14.32, "Český Krumlov"),
    Location(48.15, 17.11, "Bratislava"),
]


def _get(url: str, **kwargs) -> requests.Response:
    """GET ``url`` with a per-attempt timeout, retrying transient failures.

    Network timeouts and 5xx responses from the Open-Meteo endpoints are
    retried with exponential backoff (see ``REQUEST_RETRIES``); the last error
    is re-raised if every attempt fails. Keeps a single flaky response from
    failing a collection run that fetches several locations.
    """
    last_exc: Exception | None = None
    for attempt in range(REQUEST_RETRIES):
        try:
            resp = requests.get(url, timeout=REQUEST_TIMEOUT, **kwargs)
            resp.raise_for_status()
            return resp
        except requests.exceptions.RequestException as exc:
            last_exc = exc
            if attempt < REQUEST_RETRIES - 1:
                time.sleep(REQUEST_BACKOFF * (2 ** attempt))
    raise last_exc  # type: ignore[misc]


def latest_run_time(model: str = MODEL) -> dt.datetime:
    """Initialisation time (UTC) of the model's latest available run.

    The Ensemble API response carries no run timestamp — its first step is just
    the start of the returned window (always today 00:00 UTC), the *same* value
    no matter which 00/06/12/18 UTC cycle actually produced the data. The real
    run time lives in the model's static metadata, so a forecast can be keyed
    and labelled by the run that produced it rather than by the window start.
    """
    resp = _get(META_URL.format(model=model))
    ts = resp.json()["last_run_initialisation_time"]
    return dt.datetime.fromtimestamp(ts, dt.timezone.utc)


def fetch_raw(
    latitude: float,
    longitude: float,
    forecast_days: int = 11,
    variable: str = "temperature_2m",
) -> dict:
    """Return the Open-Meteo Ensemble API response as a dict.

    The payload is the API response verbatim, plus a single added
    ``model_run_time`` key (the latest run's UTC initialisation time, from
    :func:`latest_run_time`) so the forecast can be keyed and labelled by its
    actual run rather than the window start.

    The run stamp and the data come from two separate endpoints, so a new run
    landing between the two requests could mislabel the data. To avoid that, the
    data fetch is *bracketed* by two run reads: if the latest run is unchanged
    across the bracket, it provably did not change while the data was in flight,
    so the data belongs to that run. If a run lands mid-flight the reads differ
    and we retry; the rare case where retries are exhausted self-heals on the
    next collection cycle.
    """
    params = {
        "latitude": latitude,
        "longitude": longitude,
        "hourly": variable,
        "models": MODEL,
        "temporal_resolution": "native",  # 3-hourly for ECMWF IFS ENS
        "forecast_days": forecast_days,
        "timezone": "GMT",
    }
    for _ in range(RUN_FETCH_RETRIES):
        run_before = latest_run_time()
        resp = _get(ENSEMBLE_URL, params=params)
        payload = resp.json()
        run_after = latest_run_time()
        if run_before == run_after:
            break
    payload["model_run_time"] = run_after.strftime(RUN_TIME_FORMAT)
    return payload


def location_dir(data_dir: str, loc: Location) -> str:
    """Directory holding ``loc``'s archived responses."""
    return os.path.join(
        data_dir, MODEL, f"{loc.latitude:g}_{loc.longitude:g}"
    )


def content_hash(payload: dict) -> str:
    """Stable short fingerprint of a payload's *forecast content*.

    Hashes the response with the volatile and identity-bearing fields removed
    (the API's per-request ``generationtime_ms`` and the ``model_run_time`` we
    attach), so two responses carrying the same forecast hash identically no
    matter what run stamp is on them. This is the archive's real identity:
    identical content is stored — and plotted — once, even when the model-run
    metadata advances without the data changing (the cause of the original
    "3 runs, 2 curves" drift).
    """
    content = {k: v for k, v in payload.items() if k not in _VOLATILE_KEYS}
    blob = json.dumps(content, sort_keys=True, separators=(",", ":"))
    return hashlib.sha256(blob.encode()).hexdigest()[:CONTENT_HASH_LEN]


def archive_path(loc_dir: str, run: dt.datetime, digest: str) -> str:
    """Content-keyed archive path: ``<stamp>-<digest>.json``."""
    return os.path.join(loc_dir, f"{run.strftime(STAMP_FORMAT)}-{digest}.json")


def _run_stamp(name: str) -> str:
    """Run-init-time stamp embedded in archive file ``name``."""
    return name[: -len(".json")].split("-", 1)[0]


def is_run_file(name: str) -> bool:
    """True if ``name`` is a run archive file.

    Accepts both the content-keyed form ``<stamp>-<hash>.json`` and the older
    ``<stamp>.json`` form (still present on the data branch until the legacy
    files are cleaned up), so reads stay tolerant during the migration.
    """
    if not name.endswith(".json"):
        return False
    try:
        dt.datetime.strptime(_run_stamp(name), STAMP_FORMAT)
    except ValueError:
        return False
    return True


def _is_legacy_run_file(name: str) -> bool:
    """True only for the pre-content-hash ``<stamp>.json`` archive form."""
    if not name.endswith(".json"):
        return False
    try:
        dt.datetime.strptime(name[: -len(".json")], STAMP_FORMAT)
    except ValueError:
        return False
    return True


def _run_stamp_archived(loc_dir: str, run: dt.datetime) -> bool:
    """True if any archive file is keyed to ``run`` (legacy or content-keyed)."""
    if not os.path.isdir(loc_dir):
        return False
    stamp = run.strftime(STAMP_FORMAT)
    return any(is_run_file(n) and _run_stamp(n) == stamp
               for n in os.listdir(loc_dir))


def _content_archived(loc_dir: str, digest: str) -> bool:
    """True if a content-keyed file for ``digest`` already exists."""
    if not os.path.isdir(loc_dir):
        return False
    suffix = f"-{digest}.json"
    return any(n.endswith(suffix) and is_run_file(n)
               for n in os.listdir(loc_dir))


def latest_file(data_dir: str, loc: Location) -> str | None:
    """Path to the latest-run archive file for ``loc`` (or ``None``).

    Files are named by run init time, so the lexicographically last one is the
    most recent run.
    """
    d = location_dir(data_dir, loc)
    if not os.path.isdir(d):
        return None
    files = sorted(f for f in os.listdir(d) if is_run_file(f))
    return os.path.join(d, files[-1]) if files else None


def load_latest(data_dir: str, loc: Location) -> dict:
    """Load the latest-run raw payload for ``loc``."""
    path = latest_file(data_dir, loc)
    if path is None:
        raise FileNotFoundError(
            f"no archived data for {loc.latitude}, {loc.longitude} in {data_dir}"
        )
    with open(path) as fh:
        return json.load(fh)


def load_all(data_dir: str, loc: Location) -> list[dict]:
    """Load every archived run for ``loc`` as content-distinct payloads, oldest first.

    Files are keyed by run init time, so sorting by name yields chronological
    order. Payloads with identical forecast content (see :func:`content_hash`)
    are collapsed to their earliest run, so a model-run stamp that advanced
    without the data changing — or a legacy file kept alongside its content-keyed
    copy — contributes a single run rather than a duplicate curve. Returns an
    empty list when nothing is archived yet.
    """
    d = location_dir(data_dir, loc)
    if not os.path.isdir(d):
        return []
    payloads = []
    seen: set[str] = set()
    for name in sorted(f for f in os.listdir(d) if is_run_file(f)):
        with open(os.path.join(d, name)) as fh:
            payload = json.load(fh)
        digest = content_hash(payload)
        if digest in seen:
            continue
        seen.add(digest)
        payloads.append(payload)
    return payloads


def _prune_legacy(loc_dir: str) -> None:
    """Drop pre-init-time-keyed archive files (one-time key migration).

    Older files were keyed ``<fetched-at>_<hash>.json``; left in place they
    would sort *after* the new ``<run-init-time>.json`` files and shadow the
    latest run. Only non-run-keyed ``.json`` files are removed.
    """
    for name in os.listdir(loc_dir):
        if name.endswith(".json") and not is_run_file(name):
            os.remove(os.path.join(loc_dir, name))


def _migrate_keys(loc_dir: str) -> None:
    """Give legacy ``<stamp>.json`` archives a content-keyed copy.

    For each legacy file a ``<stamp>-<hash>.json`` copy is written so existing
    data adopts the content-addressed scheme. The legacy file is left in place
    (it is read-compatible and cleaned up separately). Working oldest-first, a
    legacy file whose content already has a content-keyed copy produces none, so
    a duplicated forecast collapses to a single content-keyed archive under its
    earliest run stamp.
    """
    if not os.path.isdir(loc_dir):
        return
    for name in sorted(n for n in os.listdir(loc_dir)
                       if _is_legacy_run_file(n)):
        with open(os.path.join(loc_dir, name)) as fh:
            payload = json.load(fh)
        digest = content_hash(payload)
        if _content_archived(loc_dir, digest):
            continue
        run = dt.datetime.strptime(name[: -len(".json")], STAMP_FORMAT)
        with open(archive_path(loc_dir, run, digest), "w") as fh:
            json.dump(payload, fh, indent=2, sort_keys=True)


def collect_one(loc: Location, data_dir: str, forecast_days: int) -> str | None:
    """Archive ``loc``'s latest model run, if its forecast isn't already stored.

    Gated on the model metadata: the latest run's initialisation time is read
    first and the (expensive) ensemble download is skipped when that run is
    already archived. After downloading, the forecast's content hash is the real
    dedup key — a run whose data merely duplicates one already archived (the
    model-run stamp advanced without the data changing) is not stored again.
    Returns the new path, or ``None`` when there was nothing new to store.
    """
    loc_dir = location_dir(data_dir, loc)
    _migrate_keys(loc_dir)
    _prune_legacy(loc_dir)

    # Metadata gate: don't download the ensemble if the latest run is already
    # here. Content dedup below is the real guard; this is a pure cost-saver.
    if _run_stamp_archived(loc_dir, latest_run_time()):
        return None

    payload = fetch_raw(loc.latitude, loc.longitude, forecast_days)
    # The model-run stamp can advance while Open-Meteo still serves the same
    # forecast, so dedup on content rather than on the stamp: the earliest run
    # to carry this forecast keeps it, and the duplicate is never archived (nor
    # plotted) again. A stamp stuck ahead of the data self-heals once genuinely
    # new data lands and the content hash changes.
    digest = content_hash(payload)
    if _content_archived(loc_dir, digest):
        return None

    run = dt.datetime.fromisoformat(payload["model_run_time"])
    path = archive_path(loc_dir, run, digest)
    os.makedirs(loc_dir, exist_ok=True)
    with open(path, "w") as fh:
        json.dump(payload, fh, indent=2, sort_keys=True)
    return path


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__,
                                     formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--data-dir", default="data-store",
                        help="directory to write the archive into "
                             "(default: data-store)")
    parser.add_argument("--forecast-days", type=int, default=11,
                        help="forecast length in days (default: 11)")
    args = parser.parse_args()

    os.makedirs(args.data_dir, exist_ok=True)
    readme = os.path.join(args.data_dir, "README.md")
    if not os.path.exists(readme):
        with open(readme, "w") as fh:
            fh.write(README)

    new = 0
    for loc in LOCATIONS:
        where = loc.name or f"{loc.latitude}, {loc.longitude}"
        path = collect_one(loc, args.data_dir, args.forecast_days)
        if path is None:
            print(f"  {where}: latest run already archived; nothing to fetch")
        else:
            new += 1
            print(f"  {where}: archived {os.path.relpath(path, args.data_dir)}")

    print(f"Done: {new} new run(s) across {len(LOCATIONS)} location(s).")


if __name__ == "__main__":
    main()
