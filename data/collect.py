#!/usr/bin/env python3
"""Collect raw Open-Meteo ensemble responses into a deduplicated archive.

Each fetched API response is stored under the data directory (the raw JSON,
plus a single added ``model_run_time`` stamp — see :func:`fetch_raw`), keyed so
that re-fetching an unchanged model run does not create a duplicate. A new file
therefore appears only when the API returns genuinely new data (typically a
fresh model run) — which is exactly what lets CI commit and push to the
``data`` branch only when there is something new.

Layout::

    <data-dir>/<model>/<lat>_<lon>/<fetched-at>_<hash>.json

``<fetched-at>`` is the UTC collection time (``YYYYMMDDTHHMMSSZ``), so files
sort chronologically and the most recent one is "the latest data". The
12-character hash is a SHA-256 over the response with volatile metadata
(``generationtime_ms``) stripped out, so byte-for-byte identical forecasts
collapse to a single file regardless of when they were fetched.
"""
from __future__ import annotations

import argparse
import datetime as dt
import hashlib
import json
import os
from dataclasses import dataclass

import requests

ENSEMBLE_URL = "https://ensemble-api.open-meteo.com/v1/ensemble"
# Per-model static metadata (run timestamps etc.); the forecast payload itself
# carries no run time, so the real initialisation time is read from here.
META_URL = "https://api.open-meteo.com/data/{model}/static/meta.json"
MODEL = "ecmwf_ifs025"

# Time format used for the model-run stamp attached to each payload; matches
# Open-Meteo's own ``hourly.time`` strings (UTC, e.g. ``2026-06-22T12:00``).
RUN_TIME_FORMAT = "%Y-%m-%dT%H:%M"

# Fields that change on every request and must not affect the dedup key.
VOLATILE_KEYS = {"generationtime_ms"}

# Filename timestamp format (UTC collection time): YYYYMMDDTHHMMSSZ.
STAMP_FORMAT = "%Y%m%dT%H%M%SZ"

README = """\
# Raw forecast data archive

This orphan branch is an append-only archive of **raw Open-Meteo Ensemble API
responses**, collected automatically by the `fetch` job of the site workflow.

Each file is the API payload, plus an added `model_run_time` stamp (the run's
UTC initialisation time). Files are keyed by content hash so an unchanged model
run is never stored twice; a new file (and a new commit) only appears when the
API returns genuinely new data.

```
<model>/<lat>_<lon>/<fetched-at>_<hash>.json
```

Do not edit by hand — it is regenerated by CI.
"""


@dataclass(frozen=True)
class Location:
    latitude: float
    longitude: float
    name: str | None = None


# Points to archive. Mirrors the published site (Berlin) by default; add more
# entries here to grow the archive.
LOCATIONS = [
    Location(52.55, 13.41, "Berlin"),
]


def latest_run_time(model: str = MODEL) -> dt.datetime:
    """Initialisation time (UTC) of the model's latest available run.

    The Ensemble API response carries no run timestamp — its first step is just
    the start of the returned window (always today 00:00 UTC), the *same* value
    no matter which 00/06/12/18 UTC cycle actually produced the data. The real
    run time lives in the model's static metadata, so a forecast can be labelled
    by the run that produced it rather than by the window start.
    """
    resp = requests.get(META_URL.format(model=model), timeout=60)
    resp.raise_for_status()
    ts = resp.json()["last_run_initialisation_time"]
    return dt.datetime.fromtimestamp(ts, dt.timezone.utc)


def fetch_raw(
    latitude: float,
    longitude: float,
    forecast_days: int = 11,
    variable: str = "temperature_2m",
) -> dict:
    """Return the Open-Meteo Ensemble API response as a dict.

    The payload is the API response verbatim, plus a single added
    ``model_run_time`` key (the latest run's UTC initialisation time, from
    :func:`latest_run_time`) so downstream plotting can label the forecast by
    its actual run rather than the window start. Because the stamp changes with
    each new run, it folds naturally into the dedup hash.
    """
    params = {
        "latitude": latitude,
        "longitude": longitude,
        "hourly": variable,
        "models": MODEL,
        "temporal_resolution": "native",  # 3-hourly for ECMWF IFS ENS
        "forecast_days": forecast_days,
        "timezone": "GMT",
    }
    resp = requests.get(ENSEMBLE_URL, params=params, timeout=60)
    resp.raise_for_status()
    payload = resp.json()
    payload["model_run_time"] = latest_run_time().strftime(RUN_TIME_FORMAT)
    return payload


def content_hash(payload: dict) -> str:
    """Stable 12-char digest of ``payload`` with volatile metadata removed."""
    stable = {k: v for k, v in payload.items() if k not in VOLATILE_KEYS}
    blob = json.dumps(stable, sort_keys=True, separators=(",", ":"))
    return hashlib.sha256(blob.encode("utf-8")).hexdigest()[:12]


def location_dir(data_dir: str, loc: Location) -> str:
    """Directory holding ``loc``'s archived responses."""
    return os.path.join(
        data_dir, MODEL, f"{loc.latitude:g}_{loc.longitude:g}"
    )


def latest_file(data_dir: str, loc: Location) -> str | None:
    """Path to the most recently collected response for ``loc`` (or ``None``)."""
    d = location_dir(data_dir, loc)
    if not os.path.isdir(d):
        return None
    files = sorted(f for f in os.listdir(d) if f.endswith(".json"))
    return os.path.join(d, files[-1]) if files else None


def latest_age(data_dir: str, loc: Location) -> float | None:
    """Seconds since ``loc``'s most recent response was collected (or ``None``).

    Read straight from the latest filename's timestamp — the data key itself.
    """
    path = latest_file(data_dir, loc)
    if path is None:
        return None
    stamp = os.path.basename(path).split("_", 1)[0]
    fetched = dt.datetime.strptime(stamp, STAMP_FORMAT).replace(
        tzinfo=dt.timezone.utc
    )
    return (dt.datetime.now(dt.timezone.utc) - fetched).total_seconds()


def load_latest(data_dir: str, loc: Location) -> dict:
    """Load the most recently collected raw payload for ``loc``."""
    path = latest_file(data_dir, loc)
    if path is None:
        raise FileNotFoundError(
            f"no archived data for {loc.latitude}, {loc.longitude} in {data_dir}"
        )
    with open(path) as fh:
        return json.load(fh)


def collect_one(loc: Location, data_dir: str, forecast_days: int) -> str | None:
    """Fetch ``loc`` and archive it. Returns the new path, or ``None`` if the
    content is already present (a duplicate)."""
    payload = fetch_raw(loc.latitude, loc.longitude, forecast_days)
    digest = content_hash(payload)

    loc_dir = location_dir(data_dir, loc)
    os.makedirs(loc_dir, exist_ok=True)

    # Dedup: a file already ending in this content hash means no new data.
    for existing in os.listdir(loc_dir):
        if existing.endswith(f"_{digest}.json"):
            return None

    stamp = dt.datetime.now(dt.timezone.utc).strftime(STAMP_FORMAT)
    path = os.path.join(loc_dir, f"{stamp}_{digest}.json")
    with open(path, "w") as fh:
        json.dump(payload, fh, indent=2, sort_keys=True)
    return path


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__,
                                     formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--data-dir", default="data-store",
                        help="directory to write the archive into "
                             "(default: data-store)")
    parser.add_argument("--forecast-days", type=int, default=11,
                        help="forecast length in days (default: 11)")
    parser.add_argument("--max-age", type=float, default=None,
                        help="skip a location whose latest data is younger than "
                             "this many seconds (default: always fetch)")
    args = parser.parse_args()

    os.makedirs(args.data_dir, exist_ok=True)
    readme = os.path.join(args.data_dir, "README.md")
    if not os.path.exists(readme):
        with open(readme, "w") as fh:
            fh.write(README)

    new = 0
    for loc in LOCATIONS:
        where = loc.name or f"{loc.latitude}, {loc.longitude}"
        age = latest_age(args.data_dir, loc)
        if args.max_age is not None and age is not None and age < args.max_age:
            print(f"  {where}: latest data {age:.0f}s old "
                  f"(< {args.max_age:.0f}s); skipping fetch")
            continue
        path = collect_one(loc, args.data_dir, args.forecast_days)
        if path is None:
            print(f"  {where}: no new data (already archived)")
        else:
            new += 1
            print(f"  {where}: archived {os.path.relpath(path, args.data_dir)}")

    print(f"Done: {new} new file(s) across {len(LOCATIONS)} location(s).")


if __name__ == "__main__":
    main()
